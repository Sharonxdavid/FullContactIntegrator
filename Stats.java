
public class Stats {
	
	private int femaleCounter;
	private int maleCounter;
	private double femaleAccuracyAvg;
	private double maleAccuracyAvg;
	public int getFemaleCounter() {
		return femaleCounter;
	}
	public void setFemaleCounter(int femaleCounter) {
		this.femaleCounter = femaleCounter;
	}
	public int getMaleCounter() {
		return maleCounter;
	}
	public void setMaleCounter(int maleCounter) {
		this.maleCounter = maleCounter;
	}
	public double getFemaleAccuracyAvg() {
		return femaleAccuracyAvg;
	}
	public void setFemaleAccuracyAvg(double femaleAccuracyAvg) {
		this.femaleAccuracyAvg = femaleAccuracyAvg;
	}
	public double getMaleAccuracyAvg() {
		return maleAccuracyAvg;
	}
	public void setMaleAccuracyAvg(double maleAccuracyAvg) {
		this.maleAccuracyAvg = maleAccuracyAvg;
	}
	

}
